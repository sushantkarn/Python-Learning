# This is for l1.sort()
l1 = [5, 38, 54, 89, 55, 86, 59, 89]
l1.sort()
print(l1)


# This is for l1.reverse()
l1 = [5, 38, 54, 89, 55, 86, 59, 89]
l1.reverse()
print(l1)


# l1.append()  --> add at the end of the list
l1.append(95)  # add 95 at the end of the list
print(l1)

l1.insert(2, 455)  # inserts 455 after second index

l1.pop(2)  # remove elements at index at 2
l1.remove(54)  # remove 54 from the  element
print(l1)
