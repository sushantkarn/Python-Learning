a = 'Sushant'
b = 'Karn'
print(a + b)  # Concatenation of string
print(a[0])
print(b[0:4])  # This is called string slicing

print(b[:4])  # This is equal to b[0:4]
print(b[0:])  # This is equal to b[0: To length of string]

print(a[-4:-1])  # Negitive Indeses

print(a[0:5:2])  # Skipping with 2 strings
